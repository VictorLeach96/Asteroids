using UnityEngine;
using System.Collections;

public class DestroyCompletedParticle : MonoBehaviour
{
	////////////////////////////////////////////////////////////////////////////////////////////////////
	// Properties
	////////////////////////////////////////////////////////////////////////////////////////////////////

	ParticleSystem m_particleEmitter;

	////////////////////////////////////////////////////////////////////////////////////////////////////
	// Setup
	////////////////////////////////////////////////////////////////////////////////////////////////////

	void Awake()
	{
		m_particleEmitter = (ParticleSystem)GetComponentInChildren<ParticleSystem>();
	}

	void Update()
	{
		//Destroy the game object once particles are finished
		if (!m_particleEmitter.IsAlive())
		{
			Destroy(gameObject);
		}
	}
}
