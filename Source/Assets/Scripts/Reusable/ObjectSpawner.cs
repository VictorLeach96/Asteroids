using UnityEngine;
using System.Collections;

public class ObjectSpawner : RawObjectSpawner
{
	////////////////////////////////////////////////////////////////////////////////////////////////////
	// Properties
	////////////////////////////////////////////////////////////////////////////////////////////////////

	public ObjectPool objectPool; //Object pool to get spawned instances from
	
	////////////////////////////////////////////////////////////////////////////////////////////////////
	// Methods
	////////////////////////////////////////////////////////////////////////////////////////////////////

	//Spawn a random object from the pool with random y position
	public override void SpawnObject()
	{
		GameObject newObject = objectPool.InstantiateNextAvailableObject();
		newObject.transform.position = transform.position - new Vector3(2f, Random.Range(0f, yRange) - (yRange / 2), 0f);
	}
}
