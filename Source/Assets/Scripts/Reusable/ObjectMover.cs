using UnityEngine;
using System.Collections;

public class ObjectMover : MonoBehaviour
{
	////////////////////////////////////////////////////////////////////////////////////////////////////
	// Properties
	////////////////////////////////////////////////////////////////////////////////////////////////////

	public Vector2 moveRange = new Vector2(1f, 4f);

	////////////////////////////////////////////////////////////////////////////////////////////////////
	// Setup
	////////////////////////////////////////////////////////////////////////////////////////////////////

	void Update()
	{
		transform.Translate (Vector3.left * Time.deltaTime * Random.Range(moveRange.x, moveRange.y));
	}
}
